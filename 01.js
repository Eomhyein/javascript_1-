// 메모리에 저장, 읽어서 재사용 : 변수
// 변수이름 : 저장되는 값이 고유이름
// 변수 값 : 변수에 저장된 값
// 변수 할당 : 변수에 저장된 값
// 변수 선언 : 변수를 사용하기 위해 컴퓨터에 알리는 행위
// 변수 참조 : 변수에 할당된 값을 읽어오는 것

// 변수를 선언할 수 있는 3가지 방법 : var, let, const
//1. Var
var myVar = "Var";
var myVar = "test1";
myVar = "Apple"
console.log(myVar);

// 2. Let
let myLet = "Let";
// 이미 오류가 발생되어짐
// let myLet= "test2";
myLet = "Orange";
console.log(myLet);

// 3. Const
const myConst = "Const";
// const myConst = "test3";
myConst = "Grape";
console.log(myConst);
