let fruit = "사과";
switch (fruit) {
    case "사과":
        console.log("사과는 빨간색입니다.");
        break;
    case "바나나":
        console.log("바나나는 노란색입니다.");
        break;
    case "오렌지":
        console.log("오렌지는 주황색입니다.");
        break;
    default:
        console.log("해당하는 과일이 없습니다.");
        break;
}